// ---- SQL helpers --------------------------------------------------------

/** Quote a string literal, SQLite-style (single quotes, '' escaping). */
function s(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}
function n(value) {
  if (value === null || value === undefined) return 'NULL';
  return Number(value).toString();
}

// ---- SQL output ---------------------------------------------------------

const lines = [];
lines.push('-- Seed data for the inaugural Kamala retreat.');
lines.push('-- Generated by scripts/seed.mjs — do not hand-edit.');
lines.push('-- Apply only AFTER running drizzle migrations.');
lines.push('');
lines.push('BEGIN TRANSACTION;');
lines.push('');

// Location
lines.push('-- Location: inaugural private estate (address TBC).');
lines.push(
  `INSERT OR IGNORE INTO locations (slug, name, summary, country, region, address, tbc_address, listing_url, lat, lng) VALUES (`,
);
