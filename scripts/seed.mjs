#!/usr/bin/env node
/**
 * Seed-SQL generator for the inaugural Kamala retreat.
 *
 * This script does NOT touch the database. It prints a single block of
 * idempotent-ish SQL to stdout that can be piped into:
 *
 *   node scripts/seed.mjs > drizzle/seed.sql
 *   wrangler d1 execute kamala --local  --file=drizzle/seed.sql
 *   wrangler d1 execute kamala --remote --file=drizzle/seed.sql
 *
 * Notes
 * -----
 * - The schema migration must be applied first (see docs/db.md).
 * - All money is in pence. Prices are taken from docs/brand-context.md.
 * - The Airbnb estate address is intentionally omitted (`tbc_address = 1`)
 *   until Holly confirms the listing booking. Lat/lng are placeholder zeros.
 * - Sound healing + cacao/reishi practitioners are still TBC; the row is
 *   created with `is_tbc = 1` but only one TBC slot is seeded for clarity.
 * - Year of inaugural weekend is 2026 (per project_decisions.md).
 *
 * Re-running
 * ----------
 * The INSERTs use `INSERT OR IGNORE` keyed on the relevant unique indexes
 * (location.slug, retreat.slug, practitioner.slug, room_types.(retreat_id,
 * code), add_ons.(retreat_id, code)) so re-running is safe but will not
 * mutate existing rows.
 */

// ---- Reference values ---------------------------------------------------

// Friday 7pm London → Sunday 3pm London, 2-4 October 2026.
// London is BST (UTC+1) on those dates.
//  Fri 2026-10-02 19:00 BST = 2026-10-02 18:00 UTC = epoch 1790964000
//  Sun 2026-10-04 15:00 BST = 2026-10-04 14:00 UTC = epoch 1791122400
